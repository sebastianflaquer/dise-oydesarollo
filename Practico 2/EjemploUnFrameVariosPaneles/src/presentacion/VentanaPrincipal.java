    //Agrego las acciones correspondientes al menu de la aplicacion
    public void agregarAccionesMenu(){
        ActionListener al = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                ejecutarAccionMenu(((Component)e.getSource()).getName());
            }
        };
        for (int i = 0; i < this.barraDeMenu.getComponents().length; i++) {
            JMenu menu = this.barraDeMenu.getMenu(i);
            
            for (int j = 0; j < menu.getItemCount(); j++) {
                menu.getItem(j).addActionListener(al);
            }
        }
    }
    
    //analizo que opcion fue seleccionada y disparo el evento
    private void ejecutarAccionMenu(String action) {
        switch(action)
        {
            case "NuevaPelicula":
                this.initNuevaPelicula();
                break;
            case "ListadoAlfabetico":
                this.initListadoAlfabetico();
                break;
            default:
                break;
        }
    }
    
    
    private void initNuevaPelicula()
    {
        PeliculaFrm frmAgregarPelicula = new PeliculaFrm();
        
        ActionListener eventoAgregarPelicula = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                PeliculaFrm p = (PeliculaFrm)((Component)e.getSource()).getParent();
                Modelo.GetInstancia().AgregarPelicula(p.GetPelicula());
                p.LimpiarPantalla();
            }
        };
        
        frmAgregarPelicula.agregarAccionGuardar(eventoAgregarPelicula);
        agregarVentana(frmAgregarPelicula);
    }
    
    private void initListadoAlfabetico()
    {
        ListadoPeliculasFrm frmLisatdoPeliculas = new ListadoPeliculasFrm();
        agregarVentana(frmLisatdoPeliculas);
        frmLisatdoPeliculas.Actualizar(Modelo.GetInstancia().getListadoPeliculasOrdenado());
    }
    
    //Aca pongo en la ventana cada nuevo panel
    public void agregarVentana(JPanel ventana){
        this.getContentPane().removeAll(); //borro todos los paneles
        this.getContentPane().add(ventana); //agrego el nuevo
        
        ventana.setSize(new Dimension(
           ventana.getParent().getWidth(),
           ventana.getParent().getHeight())); 
                
        this.getContentPane().repaint(); //re dibujo el panel principal
        this.getContentPane().revalidate(); //actualizo punteros
    }
    
